int		ft_sqrt(int nb)
{
	int r;

	r = 1;
	if (nb == 0)
		return (0);
	while (r * r < nb && r <= 46340)
	{
		r++;
	}
	if (r * r == nb)
		return (r);
	else
		return (0);
}
