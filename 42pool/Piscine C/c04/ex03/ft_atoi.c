int		ft_atoi(char *str)
{
	int i;
	int nb;
	int c;

	i = 0;
	nb = 0;
	c = 1;
	while ((str[i] == '\t') || (str[i] == '\v')
			|| (str[i] == '\n') || (str[i] == '\r') ||
			(str[i] == '\f') || (str[i] == ' '))
		i++;
	while ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			c = c * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	return (nb * c);
}
